/**
 * @brief Moves a stepper motor using hardware timers for precise RTOS delays
 * @param steps: Number of steps to take
 * @param direction: 1 for CW, 0 for CCW
 * @param speed_us: Microseconds between pulses (lower is faster)
 * @param STEP_Port: GPIO port for Step (e.g., GPIOA)
 * @param STEP_Pin: GPIO pin for Step
 * @param DIR_Port: GPIO port for Direction
 * @param DIR_Pin: GPIO pin for Direction
 * @param htim: Hardware Timer handle (e.g., &htim1)
 */
void Step_Motor(int steps, uint8_t direction, uint16_t speed_us, 
                GPIO_TypeDef* STEP_Port, uint16_t STEP_Pin, 
                GPIO_TypeDef* DIR_Port, uint16_t DIR_Pin,
                TIM_HandleTypeDef *htim) 
{
    // 1. Set Direction
    HAL_GPIO_WritePin(DIR_Port, DIR_Pin, direction ? GPIO_PIN_SET : GPIO_PIN_RESET);
    
    // 2. Pulse Loop
    for (int i = 0; i < steps; i++) {
        // Step HIGH
        HAL_GPIO_WritePin(STEP_Port, STEP_Pin, GPIO_PIN_SET);
        
        // Hardware Microsecond Delay (Replaces software delay loop)
        __HAL_TIM_SET_COUNTER(htim, 0);
        while (__HAL_TIM_GET_COUNTER(htim) < speed_us);
        
        // Step LOW
        HAL_GPIO_WritePin(STEP_Port, STEP_Pin, GPIO_PIN_RESET);
        
        // Hardware Microsecond Delay
        __HAL_TIM_SET_COUNTER(htim, 0);
        while (__HAL_TIM_GET_COUNTER(htim) < speed_us);
    }
}
